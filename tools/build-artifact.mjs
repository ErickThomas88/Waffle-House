// =====================================================================
// build-artifact.mjs — inline css/ and js/ into a single self-contained
// HTML file (waffle-house.html) so the app can run as a Claude artifact
// or any standalone page with no external file references.
//   run with:  node tools/build-artifact.mjs
// =====================================================================
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const read = (p) => readFileSync(join(root, p), "utf8");

// Keep <script> bodies from prematurely closing the host <script> tag.
const safe = (js) => js.replace(/<\/script>/gi, "<\\/script>");

// Sandboxed iframes (like Claude artifacts) can make localStorage throw on
// access. Install an in-memory fallback so the app never crashes there.
const storageGuard = `
  (function () {
    try {
      var t = "__wh_test__";
      window.localStorage.setItem(t, "1");
      window.localStorage.removeItem(t);
    } catch (e) {
      var mem = {};
      var shim = {
        getItem: function (k) { return Object.prototype.hasOwnProperty.call(mem, k) ? mem[k] : null; },
        setItem: function (k, v) { mem[k] = String(v); },
        removeItem: function (k) { delete mem[k]; },
        clear: function () { mem = {}; },
        key: function (i) { return Object.keys(mem)[i] || null; },
        get length() { return Object.keys(mem).length; }
      };
      try { Object.defineProperty(window, "localStorage", { value: shim, configurable: true }); }
      catch (e2) { try { window.localStorage = shim; } catch (e3) {} }
    }
  })();`;

const css = read("css/styles.css");
const scripts = ["data/prices.js", "js/parser.js", "js/ticket.js", "js/speech.js", "js/app.js"];

let html = read("index.html");

// 1) inline the stylesheet
html = html.replace(
  /<link rel="stylesheet" href="css\/styles\.css">/,
  `<style>\n${css}\n  </style>`
);

// 2) inline each external script (in original order)
scripts.forEach((src) => {
  const tag = new RegExp(`<script src="${src.replace(/\//g, "\\/")}"><\\/script>`);
  html = html.replace(tag, `<script>\n${safe(read(src))}\n  </script>`);
});

// 3) inject the storage guard as the very first script, right after <body>
html = html.replace(/<body>/, `<body>\n  <script>${storageGuard}\n  </script>`);

// 4) friendly header comment
const banner = `<!--
  Waffle House Order Pad — single-file build (generated by tools/build-artifact.mjs).
  Self-contained: no external CSS/JS. Safe to run as a Claude artifact or any
  standalone HTML page. Edit the source files in css/ and js/, then rebuild.
-->\n`;
html = html.replace(/<!DOCTYPE html>\n/, `<!DOCTYPE html>\n${banner}`);

writeFileSync(join(root, "waffle-house.html"), html);

const lines = html.split("\n").length;
console.log(`wrote waffle-house.html (${lines} lines, ${(html.length / 1024).toFixed(1)} KB)`);
if (/<script src=/.test(html) || /<link rel="stylesheet"/.test(html)) {
  console.error("FAIL: external references remain in the bundle");
  process.exit(1);
}
console.log("ok: no external css/js references remain");
